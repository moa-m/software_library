const languageSelect = document.querySelector(".language-select");

const getStoredLanguage = () => {
    try {
        return localStorage.getItem("atomiru-language");
    } catch {
        return null;
    }
};

const storeLanguage = (language) => {
    try {
        localStorage.setItem("atomiru-language", language);
    } catch {
        // Language switching still works for the current page view.
    }
};

const setLanguage = (language) => {
    const dictionary = translations[language] ?? translations.ja;
    const metaDescription = document.querySelector("meta[name='description']");
    const ogTitle = document.querySelector("meta[property='og:title']");
    const ogDescription = document.querySelector("meta[property='og:description']");
    const twitterTitle = document.querySelector("meta[name='twitter:title']");
    const twitterDescription = document.querySelector("meta[name='twitter:description']");

    document.documentElement.lang = language;
    document.title = dictionary.metaTitle;

    if (metaDescription) {
        metaDescription.setAttribute("content", dictionary.metaDescription);
    }

    [ogTitle, twitterTitle].forEach((element) => {
        element?.setAttribute("content", dictionary.metaTitle);
    });

    [ogDescription, twitterDescription].forEach((element) => {
        element?.setAttribute("content", dictionary.metaDescription);
    });

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const value = dictionary[element.dataset.i18n];

        if (value) {
            element.innerHTML = value;
        }
    });

    document.querySelectorAll("[data-locale-screenshots]").forEach((element) => {
        element.hidden = element.dataset.localeScreenshots !== language;

        if (!element.hidden) {
            element.classList.add("is-visible");
        }
    });

    document.querySelectorAll("[data-locale-feature]").forEach((element) => {
        element.hidden = element.dataset.localeFeature !== language;

        if (!element.hidden) {
            element.classList.add("is-visible");
        }
    });

    if (languageSelect) {
        languageSelect.value = language;
    }

    storeLanguage(language);
};

const savedLanguage = getStoredLanguage();
const browserLanguage = navigator.language?.startsWith("en") ? "en" : "ja";
const initialLanguage = translations[savedLanguage] ? savedLanguage : browserLanguage;

setLanguage(initialLanguage);

languageSelect?.addEventListener("change", (event) => {
    setLanguage(event.target.value);
});

const revealTargets = document.querySelectorAll(".reveal");

if ("IntersectionObserver" in window) {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (!entry.isIntersecting) {
                return;
            }

            entry.target.classList.add("is-visible");
            observer.unobserve(entry.target);
        });
    }, {
        rootMargin: "0px 0px -10% 0px",
        threshold: 0.1
    });

    revealTargets.forEach((target) => observer.observe(target));
} else {
    revealTargets.forEach((target) => target.classList.add("is-visible"));
}
